class Ex20 { 
 
	public static void main(String[] args) { 
 
		// Switch		
		int su = 23;

		switch (su) {
			case 0:
			System.out.println("0입니다"); 
			break;
			case 1:
			System.out.println("1입니다"); 
			break;
			case 2:
			System.out.println("2입니다"); 
			break;
			case 3:
			System.out.println("3입니다"); 
			break;
			default:
			System.out.println("0 ~ 3은 아닙니다"); 
			break;
		}
 
	} // main end 
 
} // class end 
